package com.shxp.apk.domain.po;

public class ApkTelecomFileDetailPo {
    private String id;
    private String apkFilename;
    private String apkPackageName;
    private String apkFilenameAlias;
    private String apkClassName;
    private String apkPermissionName;
    private String apkOriginName;
    private String apkVersion;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getApkFilename() {
        return apkFilename;
    }

    public void setApkFilename(String apkFilename) {
        this.apkFilename = apkFilename;
    }

    public String getApkPackageName() {
        return apkPackageName;
    }

    public void setApkPackageName(String apkPackageName) {
        this.apkPackageName = apkPackageName;
    }

    public String getApkFilenameAlias() {
        return apkFilenameAlias;
    }

    public void setApkFilenameAlias(String apkFilenameAlias) {
        this.apkFilenameAlias = apkFilenameAlias;
    }

    public String getApkClassName() {
        return apkClassName;
    }

    public void setApkClassName(String apkClassName) {
        this.apkClassName = apkClassName;
    }

    public String getApkPermissionName() {
        return apkPermissionName;
    }

    public void setApkPermissionName(String apkPermissionName) {
        this.apkPermissionName = apkPermissionName;
    }

    public String getApkOriginName() {
        return apkOriginName;
    }

    public void setApkOriginName(String apkOriginName) {
        this.apkOriginName = apkOriginName;
    }

    public String getApkVersion() {
        return apkVersion;
    }

    public void setApkVersion(String apkVersion) {
        this.apkVersion = apkVersion;
    }
}
